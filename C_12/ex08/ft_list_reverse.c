#include "ft_list.h"

void	ft_list_reverse(t_list **begin_list)
{
	t_list *current;
	t_list *previous;
	t_list *next;

	current = *begin_list;
	if (!current || !(current->next))
		return ;
	previous = 0;
	next = 0;
	while (current)
	{
		next = current->next;
		current->next = previous;
		previous = current;
		current = next;
	}
	*begin_list = previous;
}
